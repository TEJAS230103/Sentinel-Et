import os
import pandas as pd
import numpy as np
import operator
from typing import Annotated, TypedDict, List, Dict
from google.colab import userdata
from langchain_google_genai import ChatGoogleGenerativeAI
from langgraph.graph import StateGraph, END

# --- STEP 1: RESILIENT INTELLIGENCE ---
try:
    os.environ["GOOGLE_API_KEY"] = userdata.get('GOOGLE_API_KEY')
    llm = ChatGoogleGenerativeAI(model="gemini-2.5-flash", temperature=0.1)
    llm.invoke("test")
    API_ONLINE = True
except:
    llm = None
    API_ONLINE = False

def safe_reasoning(prompt: str, fallback: str):
    if API_ONLINE and llm:
        try: return llm.invoke(prompt).content
        except: return fallback
    return fallback

# --- STEP 2: ENTERPRISE DATA & SAVINGS LOGIC ---
class SentinelState(TypedDict):
    signals: List[Dict]
    decision_matrix: Dict
    change_requests: List[Dict]
    audit_log: Annotated[List[str], operator.add]
    total_impact: Dict

# THE SAVINGS MODEL (Used deterministically in the code)
SAVINGS_FACTORS = {
    "Virtual Machines": 0.18, # Rightsizing Factor
    "GPU Cluster": 0.22,      # Reservation Factor
    "Storage": 0.15,          # Lifecycle Factor
    "SaaS Licenses": 0.12,    # Harvesting Factor
    "Network": 0.10           # Optimization Factor
}

RAW_DATA = pd.DataFrame({
    "service": ["Virtual Machines", "Storage", "Network", "GPU Cluster", "SaaS Licenses"],
    "monthly_cost": [240000, 82000, 54000, 212000, 96000],
    "last_month_cost": [180000, 79000, 52000, 170000, 94000],
    "criticality": [0.9, 0.7, 0.8, 1.0, 0.6]
})

SERVICE_FIXES = {
    "Virtual Machines": "[FIX] Instance Rightsizing & Auto-scaling\n[CLI] aws ec2 modify-instance-attribute --instance-type t3.medium",
    "Storage": "[FIX] S3 Lifecycle Policy (Transition to IA/Glacier)\n[CLI] aws s3api put-bucket-lifecycle-configuration",
    "Network": "[FIX] Bandwidth Optimization & NAT Gateway Audit\n[CLI] aws ec2 replace-route --destination-cidr-block 0.0.0.0/0",
    "GPU Cluster": "[FIX] Reserved Instance Purchase (1-Year Term)\n[CLI] aws ec2 purchase-reserved-instances-offering",
    "SaaS Licenses": "[FIX] License Harvesting & Unused Seat Deactivation\n[PROCESS] Deactivate inactive user SSO profiles"
}

DEPENDENCIES = {"GPU Cluster": ["Storage", "Network"], "Virtual Machines": ["Storage"]}

# --- STEP 3: THE MULTI-AGENT SWARM ---

def sentry_agent(state: SentinelState):
    """AGENT 1: The Sentry - Perception Layer."""
    signals = [
        {"cat": "compute", "rel": 0.95, "name": "GPU Shortage / Supply Chain Shock"},
        {"cat": "infra", "rel": 0.85, "name": "Energy Inflation / Mumbai Surcharge"}
    ]
    return {"signals": signals, "audit_log": ["Sentry: Detected 2 External Economic Drivers from ET News."]}

def decision_twin_agent(state: SentinelState):
    """AGENT 2: The Quant - Risk Modeling & Dependency Propagation."""
    df = RAW_DATA.copy()
    df["growth"] = (df["monthly_cost"] - df["last_month_cost"]) / df["last_month_cost"]

    # Financial Risk Priority (FRP) = log(Cost) * Growth * Criticality
    df["base_frp"] = np.log10(df["monthly_cost"]) * df["growth"] * df["criticality"]

    # 1. RISK PROPAGATION
    spillover = {s: 0.0 for s in df["service"]}
    for i, row in df.iterrows():
        if row['service'] in DEPENDENCIES:
            for dep in DEPENDENCIES[row['service']]:
                spillover[dep] += row['base_frp'] * 0.25

    df["spillover"] = [spillover[s] for s in df["service"]]
    df["final_frp"] = df["base_frp"] + df["spillover"]

    # 2. DYNAMIC SAVINGS CALCULATION (Fixes your issue!)
    # We apply the specific factor from SAVINGS_FACTORS to the monthly cost
    df["est_savings"] = [int(row['monthly_cost'] * SAVINGS_FACTORS.get(row['service'], 0.10)) for i, row in df.iterrows()]

    df["confidence"] = [0.89 if s == "GPU Cluster" else 0.78 for s in df["service"]]
    df["primary_driver"] = ["Energy Inflation" if s != "GPU Cluster" else "GPU Shortage" for s in df["service"]]

    # At-Risk Spend Analysis
    high_risk_df = df[df["final_frp"] > 0.05]
    at_risk_total = int(high_risk_df["monthly_cost"].sum())
    savings_total = int(high_risk_df["est_savings"].sum())

    return {
        "decision_matrix": df.to_dict(),
        "total_impact": {"at_risk": at_risk_total, "sav": savings_total},
        "audit_log": ["Decision Twin: Applied service-specific savings factors (Reservations/Lifecycle) to calculate live ROI."]
    }

def governor_agent(state: SentinelState):
    """AGENT 3: The Governor - Contextual Remediation & Safety."""
    df = pd.DataFrame(state['decision_matrix'])
    high_risk = df[df['final_frp'] > 0.05].sort_values(by="final_frp", ascending=False)

    actions = []
    for _, row in high_risk.iterrows():
        plan_base = SERVICE_FIXES.get(row['service'], "[FIX] General Optimization Required.")

        prompt = f"Explain risk for {row['service']} due to {row['primary_driver']} in 1 line."
        fallback = f"Exposure high due to {row['primary_driver']} impacts on operational costs."
        brief = safe_reasoning(prompt, fallback)

        actions.append({
            "service": row['service'],
            "frp": round(row['final_frp'], 3),
            "spill": round(row['spillover'], 3),
            "savings": row['est_savings'],
            "conf": row['confidence'],
            "plan": plan_base,
            "brief": brief.strip(),
            "status": "🔴 CRITICAL" if row['final_frp'] > 1.0 else "🟡 OPTIMIZE"
        })
    return {"change_requests": actions, "audit_log": ["Governor: Generated 5 domain-specific remediation payloads."]}

# --- STEP 4: ORCHESTRATION & DASHBOARD ---

builder = StateGraph(SentinelState)
builder.add_node("sentry", sentry_agent); builder.add_node("twin", decision_twin_agent); builder.add_node("gov", governor_agent)
builder.set_entry_point("sentry"); builder.add_edge("sentry", "twin"); builder.add_edge("twin", "gov"); builder.add_edge("gov", END)
swarm = builder.compile()

# --- STEP 5: FINAL PROFESSIONAL OUTPUT ---

output = swarm.invoke({"audit_log": []})
impact = output['total_impact']
df_matrix = pd.DataFrame(output['decision_matrix'])

print(f"\n" + "="*95)
print(f"💰 SENTINEL-ET: AUTONOMOUS ENTERPRISE COST INTELLIGENCE")
print(f"="*95)
print(f"TOTAL AT-RISK SPEND ANALYZED: ₹{impact['at_risk']:,} | EST. SAVINGS (Live Model): ₹{impact['sav']:,}")
print(f"FRP SCALE: >1.0 Critical | 0.5–1.0 Optimize | <0.5 Monitor")
print("-" * 95)

# 1. THE RISK & SAVINGS MATRIX (Now shows individual savings!)
print("\n📊 FINANCIAL RISK PRIORITY (FRP) & SAVINGS MATRIX")
print(df_matrix[['service', 'base_frp', 'spillover', 'final_frp', 'est_savings', 'confidence']].sort_values(by="final_frp", ascending=False).to_string(index=False))

# 2. EXECUTIVE REMEDIATION DASHBOARD
print("\n🛠️ AUTONOMOUS CHANGE REQUESTS (CR)")
for cr in output['change_requests']:
    icon = "🔥" if "CRITICAL" in cr['status'] else "⚡"
    print(f"\n{icon} TARGET: {cr['service'].upper()} (Confidence: {cr['conf']})")
    print(f"   WHY: {cr['brief']}")
    print(f"   💸 OPPORTUNITY: Potential savings of ₹{cr['savings']:,}")
    if cr['spill'] > 0: print(f"   🔗 SPILLOVER: Received +{cr['spill']} risk from Upstream Dependency.")
    print(f"   ACTION: {cr['plan']}")
    print("-" * 75)

print("\n✅ Final Audit Trail logged. Decision state: COMPLETED.")